// ১. একটি সাধারণ স্ট্রাক্ট তৈরি করুন
struct Number {
    value: i32,
}

// ২. স্ট্রাক্ট-এর উপর ইমপ্লিমেন্টেশন ব্লক তৈরি করুন
impl Number {
    // এইটি হলো অ্যাসোসিয়েটেড ফাংশন (যা কনস্ট্রাকটর হিসেবে কাজ করে)
    // এটি `&self` ব্যবহার করে না, বরং নতুন ডেটা নিয়ে আসে।
    pub fn new(input: i32) -> Number {
        // একটি নতুন 'Number' ইনস্ট্যান্স রিটার্ন করা হচ্ছে
        Number { value: input }
    }

    // এটি একটি **মেথড**, যা ইনস্ট্যান্সের মান দ্বিগুণ করে
    pub fn double(&self) -> i32 {
        self.value * 2
    }
}

fn main() {
    // অ্যাসোসিয়েটেড ফাংশনটি **টাইপের** নাম এবং `::` ব্যবহার করে কল করুন।
    // এটি কোনো বিদ্যমান ইনস্ট্যান্সের উপর নির্ভর করে না।
    let my_num = **Number::new * *(15);

    // মেথডটি ইনস্ট্যান্সের (my_num) উপর কল করা হয়।
    println!("মান: {}", my_num.value);
    println!("দ্বিগুণ মান: {}", my_num.double());
}
